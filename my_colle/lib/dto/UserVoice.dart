class UserVoice {
  int userNo;
  String userName;
  int collectionNo;
  String comment;
  String postTime;
  
  UserVoice(int userNo, String userName, int collectionNo, String comment, String postTime) {
    this.userNo = userNo;
    this.userName = userName;
    this.collectionNo = collectionNo;
    this.comment = comment;
    this.postTime = postTime;
  }
}